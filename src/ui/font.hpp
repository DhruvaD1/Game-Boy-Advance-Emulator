inline SDL_Texture* create_font_texture(SDL_Renderer* renderer) {
    const int GLYPH_W = 8;
    const int GLYPH_H = 8;
    const int NUM_GLYPHS = 95;
    const int TEX_W = NUM_GLYPHS * GLYPH_W;
    const int TEX_H = GLYPH_H;

    SDL_Texture* tex = SDL_CreateTexture(renderer,
        SDL_PIXELFORMAT_RGBA8888, SDL_TEXTUREACCESS_STATIC, TEX_W, TEX_H);
    if (!tex) return nullptr;

    SDL_SetTextureBlendMode(tex, SDL_BLENDMODE_BLEND);

    uint32_t pixels[TEX_W * TEX_H];
    for (int i = 0; i < TEX_W * TEX_H; i++) pixels[i] = 0;

    for (int g = 0; g < NUM_GLYPHS; g++) {
        for (int row = 0; row < GLYPH_H; row++) {
            uint8_t bits = FONT_8X8[g][row];
            for (int col = 0; col < GLYPH_W; col++) {
                if (bits & (0x80 >> col)) {
                    pixels[row * TEX_W + g * GLYPH_W + col] = 0xFFFFFFFF;
                }
            }
        }
    }

    SDL_UpdateTexture(tex, nullptr, pixels, TEX_W * sizeof(uint32_t));
    return tex;
}
